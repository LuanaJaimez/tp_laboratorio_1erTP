#ifndef RESULTADOS_H_INCLUDED
#define RESULTADOS_H_INCLUDED

/** \brief Imprime el resultado de la suma entre dos numeros
 *
 * \param numA int
 * \param numB int
 * \param result int
 *
 */
void mostrarSuma(int numA, int numB, int result);


/** \brief Imprime el resultado de la resta entre dos numeros
 *
 * \param numA int
 * \param numB int
 * \param result int
 *
 */
void mostrarResta(int numA, int numB, int result);


/** \brief Imprime el resultado de la division entre dos numeros
 *
 * \param numA int
 * \param numB int
 * \param result float
 *
 */
void mostrarDivision(int numA, int numB, float result);


/** \brief Imprime el resultado de la multiplicacion entre dos numeros
 *
 * \param numA int
 * \param numB int
 * \param result int
 *
 */
void mostrarMultiplicacion(int numA, int numB, int result);


/** \brief Imprime el resultado del factorial de dos numeros
 *
 * \param numA int
 * \param numB int
 * \param resultA int
 * \param resultB int
 *
 */
void mostrarFactorial(int numA, int numB, int resultA, int resultB);

#endif // RESULTADOS_H_INCLUDED
